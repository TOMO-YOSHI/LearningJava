package linkedin;

public class SwitchTest {

	public static void main(String[] args) {
		
		int star = 4;
		
		switch(star) {
		
		case 1:
			System.out.println("This is a cheap hotel.");
			break;
		
		case 5:
			System.out.println("This is a high-end hotel.");
			break;
		
		default:
			System.out.println("No result");
			break;
		}

	}

}
